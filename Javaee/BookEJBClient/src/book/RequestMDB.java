package book;

import java.io.Serializable;

/**
 *
 * @author giovanni
 */
public class RequestMDB implements Serializable{
    
    private  long id;
    private float price;

    public RequestMDB(long id, float price) {
        this.id = id;
        this.price = price;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }
}
